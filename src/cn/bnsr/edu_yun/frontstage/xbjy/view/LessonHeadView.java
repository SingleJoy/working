package cn.bnsr.edu_yun.frontstage.xbjy.view;


public class LessonHeadView {


	private int memberTotal;//成员总数
	private int problemTotal;//问题研讨总数
	private int articleTotal;//文章资源总数
	private int accomplishedTotal;//完成任务
	private int taskTotal;//任务总数
	
	private long lessonTotalAppraise;//评价总数
	private int lessonTotalScore;//评价总分
	public int getMemberTotal() {
		return memberTotal;
	}
	public void setMemberTotal(int memberTotal) {
		this.memberTotal = memberTotal;
	}
	public int getProblemTotal() {
		return problemTotal;
	}
	public void setProblemTotal(int problemTotal) {
		this.problemTotal = problemTotal;
	}
	public int getArticleTotal() {
		return articleTotal;
	}
	public void setArticleTotal(int articleTotal) {
		this.articleTotal = articleTotal;
	}
	public int getAccomplishedTotal() {
		return accomplishedTotal;
	}
	public void setAccomplishedTotal(int accomplishedTotal) {
		this.accomplishedTotal = accomplishedTotal;
	}
	public int getTaskTotal() {
		return taskTotal;
	}
	public void setTaskTotal(int taskTotal) {
		this.taskTotal = taskTotal;
	}
	/**
	 * @return the lessonTotalAppraise
	 */
	public long getLessonTotalAppraise() {
		return lessonTotalAppraise;
	}
	/**
	 * @param lessonTotalAppraise the lessonTotalAppraise to set
	 */
	public void setLessonTotalAppraise(long lessonTotalAppraise) {
		this.lessonTotalAppraise = lessonTotalAppraise;
	}
	/**
	 * @return the lessonTotalScore
	 */
	public int getLessonTotalScore() {
		return lessonTotalScore;
	}
	/**
	 * @param lessonTotalScore the lessonTotalScore to set
	 */
	public void setLessonTotalScore(int lessonTotalScore) {
		this.lessonTotalScore = lessonTotalScore;
	}
}
