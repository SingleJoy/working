package cn.bnsr.edu_yun.frontstage.base.view;

public class BusinessView  {
	
	private Long id;

	private Long user_id;
	
	private Byte type;
	
	private String operation_description;
	
	private String operation_time;

	private Double value;
	
	private Double total_value;

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public Long getUser_id() {
		return user_id;
	}

	public void setUser_id(Long user_id) {
		this.user_id = user_id;
	}

	public Byte getType() {
		return type;
	}

	public void setType(Byte type) {
		this.type = type;
	}

	public String getOperation_description() {
		return operation_description;
	}

	public void setOperation_description(String operation_description) {
		this.operation_description = operation_description;
	}

	public String getOperation_time() {
		return operation_time;
	}

	public void setOperation_time(String operation_time) {
		this.operation_time = operation_time;
	}

	public Double getValue() {
		return value;
	}

	public void setValue(Double value) {
		this.value = value;
	}

	public Double getTotal_value() {
		return total_value;
	}

	public void setTotal_value(Double total_value) {
		this.total_value = total_value;
	}
	
}
