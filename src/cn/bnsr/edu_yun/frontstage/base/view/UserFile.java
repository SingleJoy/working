package cn.bnsr.edu_yun.frontstage.base.view;

/**
 * 用户上传文件基础属性配置
 * @author fangxiongwei
 * @date 2016年8月10日
 */
public class UserFile {

	private String filePaths;
	private String fileLengths;
	private String fileNames;
	private String fileImages;
	
	public String getFilePaths() {
		return filePaths;
	}
	public void setFilePaths(String filePaths) {
		this.filePaths = filePaths;
	}
	public String getFileLengths() {
		return fileLengths;
	}
	public void setFileLengths(String fileLengths) {
		this.fileLengths = fileLengths;
	}
	public String getFileNames() {
		return fileNames;
	}
	public void setFileNames(String fileNames) {
		this.fileNames = fileNames;
	}
	public String getFileImages() {
		return fileImages;
	}
	public void setFileImages(String fileImages) {
		this.fileImages = fileImages;
	}
	
	
}
