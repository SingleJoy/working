package cn.bnsr.edu_yun.frontstage.base.view;

import cn.bnsr.edu_yun.backstage.base.view.PageBase;

public class MyCollectView extends PageBase{
	private long id;//收藏的id
	private String fileName;//文件名
	private String grade;//年级
	private long upload_user;//上传人id；
	private java.util.Date collectTime;//收藏时间
	private double score;//评分
	
	private String catalogId;//栏目id
	private long userId;//用户id
	private String uploaderName;//上传人名字
	private String time;//转格式后的收藏时间
	private String size;//文件大小
	private String type;//文件类型
	
	public long getId() {
		return id;
	}
	public void setId(long id) {
		this.id = id;
	}
	public String getFileName() {
		return fileName;
	}
	public void setFileName(String fileName) {
		this.fileName = fileName;
	}
	public String getGrade() {
		return grade;
	}
	public void setGrade(String grade) {
		this.grade = grade;
	}
	public long getUpload_user() {
		return upload_user;
	}
	public void setUpload_user(long upload_user) {
		this.upload_user = upload_user;
	}
	
	public java.util.Date getCollectTime() {
		return collectTime;
	}
	public void setCollectTime(java.util.Date collectTime) {
		this.collectTime = collectTime;
	}
	public double getScore() {
		return score;
	}
	public void setScore(double score) {
		this.score = score;
	}
	
	public String getCatalogId() {
		return catalogId;
	}
	public void setCatalogId(String catalogId) {
		this.catalogId = catalogId;
	}
	public long getUserId() {
		return userId;
	}
	public void setUserId(long userId) {
		this.userId = userId;
	}
	public String getUploaderName() {
		return uploaderName;
	}
	public void setUploaderName(String uploaderName) {
		this.uploaderName = uploaderName;
	}
	public String getTime() {
		return time;
	}
	public void setTime(String time) {
		this.time = time;
	}
	public String getSize() {
		return size;
	}
	public void setSize(String size) {
		this.size = size;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}

}
