package cn.bnsr.edu_yun.frontstage.train.view;

public class CourseNoteView {

	private String note_content;
	private String username;
	private String note_time;
	private int agree;
	private String hour_title;
	private int is_agree;
	private long note_id;
	public String getNote_content() {
		return note_content;
	}
	public void setNote_content(String note_content) {
		this.note_content = note_content;
	}
	public String getUsername() {
		return username;
	}
	public void setUsername(String username) {
		this.username = username;
	}
	public String getNote_time() {
		return note_time;
	}
	public void setNote_time(String note_time) {
		this.note_time = note_time;
	}
	public int getAgree() {
		return agree;
	}
	public void setAgree(int agree) {
		this.agree = agree;
	}
	public String getHour_title() {
		return hour_title;
	}
	public void setHour_title(String hour_title) {
		this.hour_title = hour_title;
	}
	public int getIs_agree() {
		return is_agree;
	}
	public void setIs_agree(int is_agree) {
		this.is_agree = is_agree;
	}
	public long getNote_id() {
		return note_id;
	}
	public void setNote_id(long note_id) {
		this.note_id = note_id;
	}
	
}
