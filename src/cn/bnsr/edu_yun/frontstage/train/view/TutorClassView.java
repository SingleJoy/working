package cn.bnsr.edu_yun.frontstage.train.view;

import cn.bnsr.edu_yun.backstage.base.view.PageBase;

/**
 * 助教班级视图
 * @author fangxiongwei
 * @date 2017年3月13日
 */
public class TutorClassView extends PageBase {

	private Long id;
	private String name;// 班级名称
	private Integer status;// 状态：0-未发布；1-已发布；2-进行中；3-已完成；4-已结业
	private Long sourceId;//课程id/培训id
	private String sourceName;//课程/培训名称
	private String img;
	private Long userId;
	private String sourceType;// 0-课程；1-培训
	
	public Long getId() {
		return id;
	}
	public void setId(Long id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public Integer getStatus() {
		return status;
	}
	public void setStatus(Integer status) {
		this.status = status;
	}
	public Long getSourceId() {
		return sourceId;
	}
	public void setSourceId(Long sourceId) {
		this.sourceId = sourceId;
	}
	public String getSourceName() {
		return sourceName;
	}
	public void setSourceName(String sourceName) {
		this.sourceName = sourceName;
	}
	public String getImg() {
		return img;
	}
	public void setImg(String img) {
		this.img = img;
	}
	public Long getUserId() {
		return userId;
	}
	public void setUserId(Long userId) {
		this.userId = userId;
	}
	public String getSourceType() {
		return sourceType;
	}
	public void setSourceType(String sourceType) {
		this.sourceType = sourceType;
	}
	
	
	
}
