package cn.bnsr.edu_yun.backstage.base.view;

public class AreaView extends PageBase {
	
	private String id;
	
	private String ids;
	
	private String area_name;
	
	private String parentId;
	
	private Integer vNo;
	
	private String area_id;
	
	private String area_parentid;
	
	private Integer count;
	
	private Integer is_open;//项目是否在该地区开放：0--是；1--否；

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getArea_name() {
		return area_name;
	}

	public void setArea_name(String area_name) {
		this.area_name = area_name;
	}

	public String getParentId() {
		return parentId;
	}

	public void setParentId(String parentId) {
		this.parentId = parentId;
	}

	public Integer getvNo() {
		return vNo;
	}

	public void setvNo(Integer vNo) {
		this.vNo = vNo;
	}

	public String getArea_id() {
		return area_id;
	}

	public void setArea_id(String area_id) {
		this.area_id = area_id;
	}

	public String getArea_parentid() {
		return area_parentid;
	}

	public void setArea_parentid(String area_parentid) {
		this.area_parentid = area_parentid;
	}

	public Integer getCount() {
		return count;
	}

	public void setCount(Integer count) {
		this.count = count;
	}

	public Integer getIs_open() {
		return is_open;
	}

	public void setIs_open(Integer is_open) {
		this.is_open = is_open;
	}

	public String getIds() {
		return ids;
	}

	public void setIds(String ids) {
		this.ids = ids;
	}
	
}
