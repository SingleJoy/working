package cn.bnsr.edu_yun.backstage.train.view;

import java.util.Date;

import cn.bnsr.edu_yun.backstage.base.view.PageBase;

public class TopicAnswerView extends PageBase {

	
    private Long id;
    private String ids;
    
    private Long questionId;
    private Date answerTime;
    private String answerContent;
    private String answererName;//回答者姓名

 	
	public Long getId() {
		return id;
	}
	public void setId(Long id) {
		this.id = id;
	}
	public String getIds() {
		return ids;
	}
	public void setIds(String ids) {
		this.ids = ids;
	}
	public Long getQuestionId() {
		return questionId;
	}
	public void setQuestionId(Long questionId) {
		this.questionId = questionId;
	}
	public Date getAnswerTime() {
		return answerTime;
	}
	public void setAnswerTime(Date answerTime) {
		this.answerTime = answerTime;
	}
	public String getAnswerContent() {
		return answerContent;
	}
	public void setAnswerContent(String answerContent) {
		this.answerContent = answerContent;
	}
	public String getAnswererName() {
		return answererName;
	}
	public void setAnswererName(String answererName) {
		this.answererName = answererName;
	}
	
 	
 	
}
